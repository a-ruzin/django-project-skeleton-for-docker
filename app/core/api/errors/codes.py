from enum import IntEnum


class ErrorCode(IntEnum):
    # 10000–19999: auth
    NOT_AUTHORIZED = 10001
    WRONG_PASSWORD = 10002

    # 20000–29999: registration
    AUTHENTICATED_CANNOT_REGISTER = 20001
    WRONG_CONFIRMATION_CODE = 20002
    EXPIRED_REGISTRATION_REQUEST = 20003

    # 30000–39999: workspace
    WORKSPACE_NOT_EMPTY = 30001
    WORKSPACE_ACCESS_DENIED = 30002
    WORKSPACE_INSUFFICIENT_PERMISSIONS = 30003

    # 90000–99999: unexpected/unknown
    UNKNOWN = 90000
